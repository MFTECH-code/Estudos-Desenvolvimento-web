/*
18) Faça um programa que leia um número entre 0 e 10, e escreva este número por extenso. Use o comando
switch. Crie um case default que escreva ‘Número fora do intervalo.’
*/


for (let numero = 0;numero < 11; numero++) {
    switch(numero) {
        case numero = 0:
            console.log('zero');
            break;
        case numero = 1:
            console.log('um');
            break;
        case numero = 2:
            console.log('dois');
            break;
        case numero = 3:
            console.log('três');
            break;
        case numero = 4:
            console.log('quatro');
            break;
        case numero = 5:
            console.log('cinco');
            break;
        case numero = 6:
            console.log('seis');
            break;
        case numero = 7:
            console.log('sete');
            break;
        case numero = 8:
            console.log('oito');
            break;
        case numero = 9:
            console.log('nove');
            break;
        case numero = 10:
            console.log('dez');
            break;
        default:
            console.log('Número fora do intervalo...');
            break;
    }
    
}


